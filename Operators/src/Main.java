
public class Main {

	public static void main(String[] args) {

		System.out.println(3 + 4.2);
		System.out.println(3 - 5f);
		System.out.println(10 / 4.0);
		System.out.println(3 * 4.2);
		System.out.println(3 % 4);

		int a = 4;
		a = a + 2;
		System.out.println(a);
		
		a++; //posfix
		++a; // prefix
		
		System.out.println(a);

	}
}
